int MapZones::GetMapZoneLoopStartIndex(float x) {
    const auto& areaData = GetMapZoneAreaDataTable();
    int size = static_cast<int>(areaData.size());
    int index = 0;
    int left = EXCLUDED_MAP_ZONE_AREA_COUNT;
    int right = size - 1;
    bool found = false;

    while (left <= right) {
        index = (left + right) / 2;
        
        if (std::abs(areaData[index].minX - x) < 0.000001f) {
            found = true;
            break;
        } else if (areaData[index].minX < x) {
            left = index + 1;
        } else {
            right = index - 1;
        }
    }

    if (right < EXCLUDED_MAP_ZONE_AREA_COUNT || left >= size) {
        index = EXCLUDED_MAP_ZONE_AREA_COUNT - 1;
    } else {
        if (!found) {
            index = right;
        }

        while (index < size - 1 && std::abs(areaData[index + 1].minX - areaData[index].minX) < 0.000001f) {
            index++;
        }
    }

    return index;
}

MapZone MapZones::GetMapZoneAtPoint(float x, float y, float z) {
    if (!(x >= -2997.469970f && x < 2997.060058f) || 
        !(y >= -2892.969970f && y < 2993.870117f) || 
        !(z >= -242.990005f && z < 900.000000f)) {
        return static_cast<MapZone>(INVALID_MAP_ZONE_ID);
    }

    const auto& areaData = GetMapZoneAreaDataTable();
    int startIndex = GetMapZoneLoopStartIndex(x);

    for (int index = startIndex; index >= 0; index--) {
        if (x < areaData[index].maxX &&
            (y >= areaData[index].minY && y < areaData[index].maxY) &&
            (z >= areaData[index].minZ && z < areaData[index].maxZ)) {
            return areaData[index].id;
        }
    }

    return static_cast<MapZone>(INVALID_MAP_ZONE_ID);
}

