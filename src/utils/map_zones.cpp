int MapZones::GetMapZoneCount() {
    return static_cast<int>(GetMapZoneDataTable().size());
}

bool MapZones::IsValidMapZone(MapZone id) {
    int zoneId = static_cast<int>(id);
    return zoneId >= 0 && zoneId < GetMapZoneCount();
}

std::string MapZones::GetMapZoneName(MapZone id) {
    if (!IsValidMapZone(id)) {
        return {};
    }

    const auto& zoneData = GetMapZoneDataTable();
    return zoneData[static_cast<int>(id)].name;
}

bool MapZones::GetMapZoneSoundID(MapZone id, int& soundId) {
    if (!IsValidMapZone(id)) {
        return false;
    }

    const auto& zoneData = GetMapZoneDataTable();
    soundId = zoneData[static_cast<int>(id)].soundId;
    return true;
}

bool MapZones::GetMapZoneAreaCount(MapZone id, int& count) {
    if (!IsValidMapZone(id)) {
        return false;
    }

    const auto& zoneData = GetMapZoneDataTable();
    count = zoneData[static_cast<int>(id)].areaCount;
    return true;
}

int MapZones::GetMapZoneAreaPos(MapZone id, float& minX, float& minY, float& minZ, 
                                float& maxX, float& maxY, float& maxZ, int start) {
    if (!IsValidMapZone(id) || start < 0) {
        return -1;
    }

    const auto& areaData = GetMapZoneAreaDataTable();
    int size = static_cast<int>(areaData.size());

    for (int index = start; index < size; index++) {
        if (areaData[index].id == id) {
            minX = areaData[index].minX;
            minY = areaData[index].minY;
            minZ = areaData[index].minZ;
            maxX = areaData[index].maxX;
            maxY = areaData[index].maxY;
            maxZ = areaData[index].maxZ;
            return index;
        }
    }

    return -1;
}